// Operator precedence :-

// Operator precedence refers to the priority given to operators while parsing a statement that has more than one 
// operator performing operations in it. It is important to ensure the correct result and also to help the compiler 
// understand what the order of operations should be. 

let result = 2 + 3 * 4; 
console.log(result); 

let anotherResult = (2 + 3) * 4; 
console.log(anotherResult); 