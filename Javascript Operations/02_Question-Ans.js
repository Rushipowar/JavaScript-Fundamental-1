//1. Arithmetic:-
//    Perform mathematical operations.

//Example:-
let add = 5 + 10;     
let sub = 7 - 4; 
let multi = 4 * 16; 
let div = 10 / 4;    

//2. Comparison Operators:-
//     Compare values and return a Boolean result.

//Example:-
let Equal = 30 === 30;     
let NotEqual = 'a' !== 'b';  
let GreaterThan = 5 > 3;

//3. Logical Operators:-
//      Combine or manipulate Boolean values.

//Example:-
let and = true && false; 
let orr = true || false;  

//4. Assignment Operators:
//      Assign values to variables.

//Example:-
let x = 10; 
x += 5;           
let y = 3;      
y *= 2;   

//5. Unary Operators:
//     Operate on a single operand.

//Example:-
let negativeNumber = -9;   
let isTruthy = !false;  

//6. Ternary Operator :
//           Provides a concise way to write conditional statements.

//Example:-
let age = 20;
let isAdult = age >= 18 ? 'Yes' : 'No';

//7. Bitwise Operators:
//      Perform operations at the bit level.

//Example:-
let bitwiseAnd = 5 & 3;   
let bitwiseOr = 5 | 3;  
let bitwiseXor = 5 ^ 3;   
